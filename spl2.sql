create TABLE EMPLOYEE(
emp_id number GENERATED BY DEFAULT ON NULL AS IDENTITY,
name varchar2(100) not null,
dob date not null,
gender varchar2(10) not null ,
address varchar2(200) not null,
phone varchar2(20) not null,
email varchar2(50) not null,
password  varchar2(100) not null,
constraint emp_gender check (gender in ('Male','Female','Others')),
CONSTRAINT EMP_ID_PK PRIMARY KEY(EMP_ID)
);

CREATE TABLE STAFF(
STAFF_ID NUMBER PRIMARY KEY,
EMP_ID NUMBER NOT NULL,
DESIGNATION VARCHAR2(100) NOT NULL,
CONSTRAINT STAFF_EMP_ID FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE
);

CREATE SEQUENCE STAFF_SEQ START WITH 20505161;

CREATE OR REPLACE TRIGGER STAFF_TRIGGER 
BEFORE INSERT ON STAFF
FOR EACH ROW

BEGIN
  SELECT STAFF_SEQ.NEXTVAL
  INTO   :new.STAFF_ID
  FROM   dual;
END;
/

CREATE TABLE DOCTOR(
DOCTOR_ID NUMBER PRIMARY KEY,
EMP_ID NUMBER NOT NULL,
 specialty varchar2(100) NOT NULL,
  Available_hr date ,
QUALIFICATION VARCHAR2(100) NOT NULL,
CONSTRAINT DOCTOR_EMP_ID FOREIGN KEY (EMP_ID) REFERENCES EMPLOYEE
);

CREATE SEQUENCE DOCTOR_SEQ START WITH 20989001;

CREATE OR REPLACE TRIGGER DOCTOR_TRIGGER 
BEFORE INSERT ON DOCTOR
FOR EACH ROW

BEGIN
  SELECT DOCTOR_SEQ.NEXTVAL
  INTO   :new.DOCTOR_ID
  FROM   dual;
END;
/

